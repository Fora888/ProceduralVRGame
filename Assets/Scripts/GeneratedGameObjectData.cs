using UnityEngine;

public struct GeneratedGameObjectData
{
    public Vector3 midpoint, rotation, size;
    public int modellIndex;

    public GeneratedGameObjectData(Vector3 midpoint, Vector3 rotation, Vector3 size, int modellIndex)
    {
        this.midpoint = midpoint;
        this.rotation = rotation;
        this.size = size;
        this.modellIndex = modellIndex;
    }
}
